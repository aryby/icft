    <h2>Instructions for authors</h2>
    <h3>Papers Preparation</h3>
    <p>All materials submitted to DESDI'25 Conference must be original, unpublished research that is not currently under review for any other conference or journal. Papers not following these guidelines may be rejected without review. Also, submissions received after the due date, exceeding the length limit, or not appropriately structured may also not be considered.</p>
    <p>Full papers must be in English and submitted in word format. Each paper must be in the range of 10 - 20 pages.</p>
    <p>Abstract must be in English or French submitted in word format.</p>
    <p>The DESDI template for preparing a full paper and Abstract can be downloaded here ( full paper template/ Abstract template)</p>

    <h3>Peer Review Process</h3>
    <p>The conference will check plagiarism for all articles before publication. If plagiarism is observed by the scientific committee, peer reviewers, or staff members at any stage of the publication process, the author will be informed to rewrite the manuscript.</p>
    <p>If the submitted original manuscript is more than 20% plagiarized, the article will be rejected, and the same will be notified to the author.</p>
    <p>Paper content (unpublished work) must be original and relevant to one of the many conference topics.</p>
    <p>The acceptance decisions will take into account paper novelty, technical depth, elegance, and practical and theoretical impact.</p>
    <p>Each paper will be reviewed by two SC members.</p>
    <p>The acceptance decisions will take into account originality, practical & theoretical impact, and paper presentation.</p>
    <p>Changes cannot be made during the review process. If accepted, minor revisions may be made prior to final submission.</p>

    <h3>Submission</h3>
    <p>Authors are invited to choose between two submission options:</p>
    <h4>Abstract Submission</h4>
    <p>Authors who wish to present their research at the conference without pursuing publication are encouraged to submit an abstract by September 03, 2025. This option is ideal for those interested in sharing their findings and receiving feedback from peers, without the requirement to publish in the conference proceedings.</p>
    <h4>Full Paper Submission</h4>
    <p>Authors seeking publication opportunities must submit a full paper by September 03, 2025. All submissions will undergo a rigorous peer-review process to ensure academic quality and thematic relevance.</p>
    <p>Accepted papers will have the opportunity to be published in one of the following outlets:</p>
    <ul>
        <li>International Journal of Intelligent Management and Data Systems</li>
        <li>The International Journal of Economic and Management Decisions</li>
    </ul>
    <p>The Scientific Committee will recommend the most suitable publication outlet for each accepted paper based on its topic and quality. Authors will be kept informed throughout the review and selection process.</p>
